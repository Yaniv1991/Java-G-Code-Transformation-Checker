
public class Point3 {

	private float x, y, z;

	public float X() {
		return x;
	}

	public float y() {
		return y;
	}

	public float Z() {
		return z;
	}

	public Point3() {
	}

	public Point3(float x, float y) {
		this.x = x;
		this.y = y;
	}

	public Point3(float x, float y, float z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

}
